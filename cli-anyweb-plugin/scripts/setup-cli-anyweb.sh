\`\`\`

## Local Assets

- \`site.env\`: site-specific browser flags
- \`cli_anyweb/${PACKAGE_SLUG}/skills/references/\`: site references
- \`cli_anyweb/${PACKAGE_SLUG}/skills/evals/\`: replay and validation assets
EOF
