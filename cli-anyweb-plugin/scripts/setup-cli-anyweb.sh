#!/usr/bin/env bash
set -euo pipefail

SITE_NAME="${1:-}"

if [[ -z "${SITE_NAME}" ]]; then
  echo "Usage: bash scripts/setup-cli-anyweb.sh <site-name>"
  exit 1
fi

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
SITE_SLUG="$(printf '%s' "${SITE_NAME}" | tr '[:upper:]' '[:lower:]' | sed -E 's/[^a-z0-9]+/-/g; s/^-+//; s/-+$//')"
PACKAGE_SLUG="$(printf '%s' "${SITE_SLUG}" | tr '-' '_')"
CLI_NAME="cli-anyweb-${SITE_SLUG}"
SITE_ROOT="${ROOT_DIR}/sites/${SITE_SLUG}/agent-harness"
PACKAGE_DIR="${SITE_ROOT}/cli_anyweb/${PACKAGE_SLUG}"
REF_DIR="${PACKAGE_DIR}/skills/references"
EVAL_DIR="${PACKAGE_DIR}/skills/evals"
TEST_DIR="${PACKAGE_DIR}/tests"
NAMESPACE_INIT="${SITE_ROOT}/cli_anyweb/__init__.py"

mkdir -p "${REF_DIR}" "${EVAL_DIR}" "${TEST_DIR}"

SITE_ENV="${SITE_ROOT}/site.env"
SITE_README="${SITE_ROOT}/README.md"
SITE_HARNESS="${SITE_ROOT}/HARNESS.md"
SITE_SETUP="${SITE_ROOT}/setup.py"
PKG_INIT="${PACKAGE_DIR}/__init__.py"
PKG_MAIN="${PACKAGE_DIR}/__main__.py"
PKG_CLI="${PACKAGE_DIR}/${PACKAGE_SLUG}_cli.py"
PKG_README="${PACKAGE_DIR}/README.md"
SKILL_MD="${PACKAGE_DIR}/skills/SKILL.md"
TEST_INIT="${TEST_DIR}/__init__.py"
TEST_MD="${TEST_DIR}/TEST.md"
SITE_PROFILE="${REF_DIR}/${SITE_SLUG}.site-profile.md"
STARTER_FLOW="${REF_DIR}/${SITE_SLUG}.starter-flow.md"
STARTER_EVAL="${EVAL_DIR}/${SITE_SLUG}.starter.eval.yaml"
STARTER_PATH="${EVAL_DIR}/${SITE_SLUG}.starter.path.yaml"

if [[ ! -f "${SITE_ENV}" ]]; then
  cat > "${SITE_ENV}" <<EOF
# Site-specific browser flags for ${CLI_NAME}.
# Example:
# CLI_ANYWEB_AGENT_BROWSER_FLAGS='--user-agent "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/136.0.0.0 Safari/537.36"'
EOF
fi

if [[ ! -f "${SITE_README}" ]]; then
  cat > "${SITE_README}" <<EOF
